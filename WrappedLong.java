public class WrappedLong {
    
        public static void main(String[] args) {
            
            long primitiveLong = 123456789L;
    
            Long wrappedLong = Long.valueOf(primitiveLong);
    
            System.out.println("Primitive Long: " + primitiveLong);
            System.out.println("Wrapped Long: " + wrappedLong);
        }
    }
    
    

